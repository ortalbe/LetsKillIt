package hackerrank.problem.sorting;

public class Player {

    public String name;
    public int score;

    public Player(int score,String name) {
        this.name = name;
        this.score = score;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
